public class TouristDestination {

    String place_name;
    String uid;
    Double rating;
    String contact_number1;
    String contact_number2;
    Double latitude;
    Double longitude;
    String place_icon;
    String place_banner;
    String caption;
    String detail;

    public String getPlace_name() {
        return place_name;
    }

    public void setPlace_name(String place_name) {
        this.place_name = place_name;
    }

    public String getUid() {
        return uid;
    }

    public void setUid(String uid) {
        this.uid = uid;
    }

    public Double getRating() {
        return rating;
    }

    public void setRating(Double rating) {
        this.rating = rating;
    }

    public String getContact_number1() {
        return contact_number1;
    }

    public void setContact_number1(String contact_number1) {
        this.contact_number1 = contact_number1;
    }

    public String getContact_number2() {
        return contact_number2;
    }

    public void setContact_number2(String contact_number2) {
        this.contact_number2 = contact_number2;
    }

    public Double getLatitude() {
        return latitude;
    }

    public void setLatitude(Double latitude) {
        this.latitude = latitude;
    }

    public Double getLongitude() {
        return longitude;
    }

    public void setLongitude(Double longitude) {
        this.longitude = longitude;
    }

    public String getPlace_icon() {
        return place_icon;
    }

    public void setPlace_icon(String place_icon) {
        this.place_icon = place_icon;
    }

    public String getPlace_banner() {
        return place_banner;
    }

    public void setPlace_banner(String place_banner) {
        this.place_banner = place_banner;
    }

    public String getCaption() {
        return caption;
    }

    public void setCaption(String caption) {
        this.caption = caption;
    }

    public String getDetail() {
        return detail;
    }

    public void setDetail(String detail) {
        this.detail = detail;
    }
}
